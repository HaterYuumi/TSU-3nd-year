# Генерация и анализ коррелированных выборок

# Очищаем рабочее пространство
rm(list = ls())

# 1. Генерируем выборку X1 из нормального распределения
set.seed(123)  # для воспроизводимости результатов

N <- 200  # объем выборки
a1 <- 5   # математическое ожидание X1
S1 <- 2   # стандартное отклонение X1

X1 <- rnorm(N, mean = a1, sd = S1)

# 2. Задаем коэффициенты корреляции
r1 <- 0.15  # в интервале [0.1; 0.2]
r2 <- 0.75  # в интервале [0.6; 0.9]

# 3. Строим выборки X2 и X3 с заданными коэффициентами корреляции
# Параметры для X2
a2 <- 3   # математическое ожидание X2
S2 <- 1.5 # стандартное отклонение X2

# Параметры для X3
a3 <- 7   # математическое ожидание X3
S3 <- 2.5 # стандартное отклонение X3

# Генерируем независимые нормальные шумы
noise2 <- rnorm(N, mean = a2, sd = S2)
noise3 <- rnorm(N, mean = a3, sd = S3)

# Формируем коррелированные выборки
X2 <- r1 * X1 + sqrt(1 - r1^2) * noise2
X3 <- r2 * X1 + sqrt(1 - r2^2) * noise3

# 4. Строим диаграммы рассеяния
# Настраиваем параметры графиков
par(mfrow = c(1, 2))  # располагаем графики в один ряд

# Диаграмма рассеяния для X1 и X2
plot(X1, X2, 
     main = paste("Диаграмма рассеяния X1 и X2\nr =", r1),
     xlab = "X1", 
     ylab = "X2",
     pch = 19,  # тип точки
     col = "blue",
     cex = 0.7)  # размер точки
abline(lm(X2 ~ X1), col = "red", lwd = 2)  # линия регрессии

# Диаграмма рассеяния для X1 и X3
plot(X1, X3, 
     main = paste("Диаграмма рассеяния X1 и X3\nr =", r2),
     xlab = "X1", 
     ylab = "X3",
     pch = 19,
     col = "darkgreen",
     cex = 0.7)
abline(lm(X3 ~ X1), col = "red", lwd = 2)

# Возвращаем стандартные настройки графиков
par(mfrow = c(1, 1))

# 5. Вычисляем выборочные парные коэффициенты корреляции
r_hat1 <- cor(X1, X2)
r_hat2 <- cor(X1, X3)

cat("========== Результаты ==========\n")
cat("Заданные коэффициенты корреляции:\n")
cat("r1 =", r1, "\n")
cat("r2 =", r2, "\n\n")

cat("Выборочные коэффициенты корреляции:\n")
cat("r̂1 =", round(r_hat1, 4), "\n")
cat("r̂2 =", round(r_hat2, 4), "\n\n")

# 6. Проверка гипотезы о наличии корреляционной связи
# Используем t-статистику Стьюдента
# H0: ρ = 0 (корреляция отсутствует)
# H1: ρ ≠ 0 (корреляция присутствует)

t_stat1 <- r_hat1 * sqrt((N - 2) / (1 - r_hat1^2))
t_stat2 <- r_hat2 * sqrt((N - 2) / (1 - r_hat2^2))

# Критическое значение для двустороннего критерия
alpha <- 0.05  # уровень значимости
t_crit <- qt(1 - alpha/2, df = N - 2)

cat("Проверка гипотезы о наличии корреляционной связи:\n")
cat("Уровень значимости α =", alpha, "\n")
cat("Критическое значение t_крит =", round(t_crit, 4), "\n\n")

cat("Для пары X1 и X2:\n")
cat("t-статистика =", round(t_stat1, 4), "\n")
if (abs(t_stat1) > t_crit) {
  cat("|t| > t_крит → H0 отвергается\n")
  cat("Корреляционная связь статистически значима\n")
} else {
  cat("|t| ≤ t_крит → H0 не отвергается\n")
  cat("Корреляционная связь статистически не значима\n")
}

cat("\nДля пары X1 и X3:\n")
cat("t-статистика =", round(t_stat2, 4), "\n")
if (abs(t_stat2) > t_crit) {
  cat("|t| > t_крит → H0 отвергается\n")
  cat("Корреляционная связь статистически значима\n")
} else {
  cat("|t| ≤ t_крит → H0 не отвергается\n")
  cat("Корреляционная связь статистически не значима\n")
}

# Вычисляем p-value для наглядности
p_val1 <- 2 * (1 - pt(abs(t_stat1), df = N - 2))
p_val2 <- 2 * (1 - pt(abs(t_stat2), df = N - 2))

cat("\np-value для X1 и X2:", round(p_val1, 6), "\n")
cat("p-value для X1 и X3:", round(p_val2, 6), "\n\n")

# 7. Построение доверительного интервала для корреляционного коэффициента
# с помощью Z-преобразования Фишера

# Функция для расчета доверительного интервала
fisher_ci <- function(r, n, conf_level = 0.95) {
  # Z-преобразование Фишера
  z <- 0.5 * log((1 + r) / (1 - r))
  
  # Стандартная ошибка
  se <- 1 / sqrt(n - 3)
  
  # Критическое значение для нормального распределения
  z_crit <- qnorm(1 - (1 - conf_level)/2)
  
  # Доверительный интервал для z
  z_lower <- z - z_crit * se
  z_upper <- z + z_crit * se
  
  # Обратное преобразование
  r_lower <- (exp(2 * z_lower) - 1) / (exp(2 * z_lower) + 1)
  r_upper <- (exp(2 * z_upper) - 1) / (exp(2 * z_upper) + 1)
  
  return(c(lower = r_lower, upper = r_upper))
}

# Вычисляем доверительные интервалы
ci1 <- fisher_ci(r_hat1, N)
ci2 <- fisher_ci(r_hat2, N)

conf_level <- 0.95
cat("Доверительные интервалы для корреляционных коэффициентов:\n")
cat("Уровень доверия:", conf_level * 100, "%\n\n")

cat("Для ρ1 (X1 и X2):\n")
cat("Нижняя граница:", round(ci1["lower"], 4), "\n")
cat("Верхняя граница:", round(ci1["upper"], 4), "\n")
cat("Заданное значение r1 =", r1, 
    ifelse(r1 >= ci1["lower"] && r1 <= ci1["upper"], 
           "✓ попадает в интервал", "✗ не попадает в интервал"), "\n\n")

cat("Для ρ2 (X1 и X3):\n")
cat("Нижняя граница:", round(ci2["lower"], 4), "\n")
cat("Верхняя граница:", round(ci2["upper"], 4), "\n")
cat("Заданное значение r2 =", r2, 
    ifelse(r2 >= ci2["lower"] && r2 <= ci2["upper"], 
           "✓ попадает в интервал", "✗ не попадает в интервал"), "\n\n")

